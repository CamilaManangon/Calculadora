#include <stdio.h>

int Sumar(int sumando1, int sumando2){
  int suma;
  suma = sumando1 + sumando2;
  return suma;
}

int Resta(int r1, int r2){
  int resta;
  resta = r1 - r2;
  return resta;
}

int Producto(int p1, int p2){
  int prod;
  prod = p1 * p2;
  return prod;
}

float Division(int d1, int d2){
    float div;
    div = d1/d2;
    return div;
}

